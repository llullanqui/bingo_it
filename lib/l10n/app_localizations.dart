  /// In en, this message translates to:
  /// **'Saved Tables'**
  String get savedTables;

  /// No description provided for @addItemHint.
  ///
  /// In en, this message translates to:
  /// **'Write the item you think will happen'**
  String get addItemHint;

  /// No description provided for @readyToStart.
  ///
  /// In en, this message translates to:
  /// **'Ready to start?'**
  String get readyToStart;

  /// No description provided for @sureToRestart.
  ///
  /// In en, this message translates to:
  /// **'Sure you want to restart?'**
  String get sureToRestart;

  /// No description provided for @addAtLeastItems.
  ///
  /// In en, this message translates to:
  /// **'Add at least {count} items'**
  String addAtLeastItems(int count);

  /// No description provided for @yes.
  ///
  /// In en, this message translates to:
  /// **'Yes'**
  String get yes;

  /// No description provided for @no.
  ///
  /// In en, this message translates to:
  /// **'No'**
  String get no;

  /// No description provided for @ok.
  ///
  /// In en, this message translates to:
  /// **'Ok'**
  String get ok;

  /// No description provided for @letsGo.
  ///
  /// In en, this message translates to:
  /// **'Let\'s go!'**
  String get letsGo;

  /// No description provided for @cancel.
  ///
  /// In en, this message translates to:
  /// **'Cancel'**
  String get cancel;

  /// No description provided for @add.
  ///
  /// In en, this message translates to:
  /// **'Add'**
  String get add;

  /// No description provided for @restart.
  ///
  /// In en, this message translates to:
  /// **'Restart'**
  String get restart;

  /// No description provided for @saveTable.
  ///
  /// In en, this message translates to:
  /// **'Save Table'**
  String get saveTable;

  /// No description provided for @tableSaved.
  ///
  /// In en, this message translates to:
  /// **'Table saved successfully!'**
  String get tableSaved;

  /// No description provided for @youWin.
  ///
  /// In en, this message translates to:
